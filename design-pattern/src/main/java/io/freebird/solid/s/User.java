public class User {

    private String uid;

    private String name;

    private String score;

    private String height;

    public void setUid(String uid) {
        this.uid = uid;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public void setHeight(String height) {
        this.height = height;
    }
}
